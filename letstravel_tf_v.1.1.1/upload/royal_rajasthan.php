						<h3>General Information About tour</h3>
						<p>Book this tour to explore the Golden Triangle of Delhi, Agra and Jaipur and also experience more of Rajasthan in Pushkar, Jodhpur and Udaipur.</p>
						<p>With this tour, you will enjoy the popular sights and monuments like the Red Fort in Delhi, the Taj Mahal in Agra, the Amber Fort in Jaipur and also get to experience life in India as locals do.  If you want to feel local, stay local at Homestays at some destinations enroute.</p>
						<p>It is a typical two-weeks-travel route from Delhi to Udaipur through Agra and Rajasthan with one night outside of Jodhpur in a small village called Chandelao to get a feel of the rural life and a short break from the busy cities.</p>
						<p>You can also participate in additional activities in each place like a slum tour in Delhi or a cooking class in Udaipur. This tour also includes lunch/dinner with a local family and visit to a school to interact with students.  This way you can meet many locals and make friends.</p>
						<p>This trip to North India can be modified to fit any budget. Below you’ll find approximate costs for different types of travelers and an example of an itinerary. If you have a fixed time period, then we’ll add or remove destinations accordingly.</p>
							
						<h4>ITINERARY</h4>

						<h5>Day 1 - 3 Delhi</h5>
						<p>Delhi is the capital of India, a city rich in history and culture. Delhi is not only an interesting place to visit but is also ideally located to travel to a lot of other worthwhile places in northern India. Delhi is known for it's excellent metro service so it's very easy to get from one sight to another. There are many interesting historical sights but to get off the beaten path, you can choose a slum and a street food tour.</p> 
 
						<h5>Day 3 - 4 Agra</h5>
						<p>Agra, most popularly known for the Taj Mahal, is a favorite amongst travelers. As a key location in the story of the Mughal Empire, Agra is known for its beautiful monuments, courtyards and mosques. The Agra Fort is a lesser known but also beautiful sight you must visit during your stay in Agra. You can also explore the spice market of the old town and spend some time with a marble craftsmen, or nature walk in Shahjahan garden.</p>
 
						<h5>Day 4 - 6 Jaipur</h5>
						<p>Painted pink to welcome the Prince of Wales in 1853, Rajasthan’s capital city is chock-full with forts, palaces and temples. Shopaholics can lap up traditional footwear, textiles, crafts and gems.  You will be fascinated by the sheer variety of skills and local handicraft products and will have an opportunity to meet a bloc printer and henna artist.  Don’t miss out on delicious food in Jaipur!</p>

						<h5>Day 6 - 8 Pushkar</h5>
						<p>The pilgrimage town of Pushkar is also considered to be one of the oldest cities in India as according to legend the god Brahma himself created the town. For a deeply spiritual experience and to find out more about the traditions and rituals of Hinduism, this should be a must-visit for every Rajasthan traveler.  Chat with priests at the holy Ghats, make friends with plenty of cows and enjoy your stay at a 100 year old Haveli.</p>
						  
						<h5>Day 8 - 9 Chandelao</h5>
						<p>Chandelao is a small village one hour outside of Jodhpur where rural life is still practiced the traditional way. Men ploughing fields with bulls, women in traditional attire caring water pots from the village fountain and a 200 year old Haveli turned into a heritage hotel. You enjoy watching the village women at the local art and craft project making jewellery and other handicrafts.</p>
						 
						 
						<h5>Day 9 - 11  Jodhpur</h5>
						<p>Due to its numerous blue houses in the old part of Jodhpur, this city in the dessert is often referred to as the “Blue City”. The second biggest city in Rajasthan is famous for the Mehrangarh Fort, one of the largest in India with astounding history of battles and great rulers. The old town is a labyrinth of small lanes with traditional Havelis and delicious street food.</p>
						 
						 
						<h5>Day 11 - 13 Udaipur</h5>
						<p>Udaipur has been called many things: Venice of the East, the White or Lake City and the most romantic place in India. One thing is for sure: when in Rajasthan you can’t miss this town with its white marble temples and palaces, its rooftop restaurants with perfect views of the lake as well as the beautiful surrounding landscapes.</p>
						 
						 
						<h5>Day 13 - 13 Delhi</h5>
						<p>Shopping and flight back home.</p>

